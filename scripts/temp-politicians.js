async function importExpandedPoliticians() {
  console.log('🚀 Starting expanded politician import...')
  console.log(`📊 Preparing to import ${politicians.length} politicians`)

  try {
    // Check existing data
    const { data: existingPoliticians, error: checkError } = await supabase
      .from('politicians')
      .select('id, name')

    if (checkError) {
      console.error('❌ Error checking politicians table:', checkError)
      return
    }

    console.log(`📋 Found ${existingPoliticians?.length || 0} existing politicians`)

    // Transform politicians for production schema
    const transformedPoliticians = politicians.map(politician => ({
      name: politician.name,
      first_name: politician.first_name,
      last_name: politician.last_name,
      party: politician.party,
      position: politician.position,
      constituency: politician.constituency,
      political_orientation: politician.political_orientation,
      bio: politician.bio,
      birth_date: politician.birth_date,
      gender: politician.gender,
      education: politician.education,
      career_history: politician.career_history,
      key_policies: politician.key_policies,
      achievements: politician.achievements,
      controversies: politician.controversies,
      social_media: politician.social_media || {},
      contact_info: {},
      metadata: {},
      credibility_score: 100,
      transparency_score: 100,
      consistency_score: 100,
      integrity_score: 100,
      engagement_score: 100,
      total_votes: 0,
      positive_votes: 0,
      negative_votes: 0,
      is_active: politician.is_active,
      verified: politician.verified
    }))

    // Filter out politicians that already exist
    const existingNames = new Set((existingPoliticians || []).map(p => p.name))
    const newPoliticians = transformedPoliticians.filter(p => !existingNames.has(p.name))

    if (newPoliticians.length === 0) {
      console.log('✅ All politicians already exist in database')
      return
    }

    console.log(`📝 Inserting ${newPoliticians.length} new politicians...`)

    // Insert in batches to avoid timeout
    const batchSize = 50
    let insertedCount = 0

    for (let i = 0; i < newPoliticians.length; i += batchSize) {
      const batch = newPoliticians.slice(i, i + batchSize)

      const { data, error } = await supabase
        .from('politicians')
        .insert(batch)
        .select('name, party')

      if (error) {
        console.error(`❌ Error inserting batch ${Math.floor(i / batchSize) + 1}:`, error)
        continue
      }

      insertedCount += data.length
      console.log(`✅ Inserted batch ${Math.floor(i / batchSize) + 1}: ${data.length} politicians`)
    }

    console.log(`🎉 Successfully imported ${insertedCount} new politicians!`)
    console.log('💡 Database now contains comprehensive French political data')

  } catch (error) {
    console.error('💥 Import failed:', error)
    process.exit(1)
  }
}

// Run the expanded import
importExpandedPoliticians()
  .then(() => {
    console.log('🏁 Expanded import completed!')
    process.exit(0)
  })
  .catch(error => {
    console.error('💀 Script failed:', error)
    process.exit(1)
  })